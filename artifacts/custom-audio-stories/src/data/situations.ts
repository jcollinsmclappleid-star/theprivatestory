/**
 * The Situation — 200 story situations across 10 categories (20 per category).
 * Frontend display-only data (no internalInject — that lives on the server).
 * Keyed by unique ID (e.g. "fc_01") — labels are display-only.
 *
 * Template tokens (pronoun placeholders):
 *   {P}    She/He/They       protagonist subject, capitalised
 *   {p}    she/he/they       protagonist subject, lowercase
 *   {Pp}   Her/His/Their     protagonist possessive, capitalised (sentence-start)
 *   {pp}   her/his/their     protagonist possessive, lowercase
 *   {po}   her/him/them      protagonist object
 *   {pr}   herself/himself/themselves  protagonist reflexive
 *   {P's}  She's/He's/They're
 *   {p's}  she's/he's/they're
 *   {p'd}  she'd/he'd/they'd
 *   {A}    He/She/They       partner subject, capitalised
 *   {a}    he/she/they       partner subject, lowercase
 *   {Ap}   His/Her/Their     partner possessive, capitalised (sentence-start)
 *   {ap}   his/her/their     partner possessive, lowercase
 *   {ao}   him/her/them      partner object
 *   {ar}   himself/herself/themselves  partner reflexive
 *   {A's}  He's/She's/They're
 *   {a's}  he's/she's/they're
 *   {a'd}  he'd/she'd/they'd
 *   {p:X/Y}  X for she/he, Y for they  (protagonist verb agreement)
 *   {a:X/Y}  X for he/she, Y for they  (partner verb agreement)
 */

export interface SituationDisplay {
  id: string;
  label: string;
  template: string;
  category: string;
}

// ---------------------------------------------------------------------------
// Interpolation
// ---------------------------------------------------------------------------

interface PCtx {
  sub: string; subL: string; poss: string; possL: string; obj: string; ref: string;
  contr: string; contrL: string; dep: string; vSuffix: string;
}

const PMAP: Record<string, PCtx> = {
  "she/her":   { sub: "She",  subL: "she",  poss: "Her",   possL: "her",   obj: "her",  ref: "herself",    contr: "She's",   contrL: "she's",   dep: "she'd",  vSuffix: "s" },
  "he/him":    { sub: "He",   subL: "he",   poss: "His",   possL: "his",   obj: "him",  ref: "himself",    contr: "He's",    contrL: "he's",    dep: "he'd",   vSuffix: "s" },
  "they/them": { sub: "They", subL: "they", poss: "Their", possL: "their", obj: "them", ref: "themselves", contr: "They're", contrL: "they're", dep: "they'd", vSuffix: "" },
};

export function interpolateSituation(
  sit: SituationDisplay,
  protagonistPronouns: string,
  partnerPronouns: string,
): string {
  const prot = PMAP[protagonistPronouns] ?? PMAP["she/her"];
  const part = PMAP[partnerPronouns]     ?? PMAP["he/him"];

  return sit.template
    .replace(/\{p:([^/}]+)\/([^}]+)\}/g, (_, s, p) => prot.vSuffix ? s : p)
    .replace(/\{a:([^/}]+)\/([^}]+)\}/g, (_, s, p) => part.vSuffix ? s : p)
    .replace(/\{P's\}/g, prot.contr)
    .replace(/\{p's\}/g, prot.contrL)
    .replace(/\{A's\}/g, part.contr)
    .replace(/\{a's\}/g, part.contrL)
    .replace(/\{p'd\}/g, prot.dep)
    .replace(/\{a'd\}/g, part.dep)
    .replace(/\{Pp\}/g,  prot.poss)
    .replace(/\{pp\}/g,  prot.possL)
    .replace(/\{po\}/g,  prot.obj)
    .replace(/\{pr\}/g,  prot.ref)
    .replace(/\{Ap\}/g,  part.poss)
    .replace(/\{ap\}/g,  part.possL)
    .replace(/\{ao\}/g,  part.obj)
    .replace(/\{ar\}/g,  part.ref)
    .replace(/\{P\}/g,   prot.sub)
    .replace(/\{p\}/g,   prot.subL)
    .replace(/\{A\}/g,   part.sub)
    .replace(/\{a\}/g,   part.subL);
}

